
public class Receptionist {
   private int rescepid;
   private String rname;
   
public int getRescepid() {
	return rescepid;
}
public void setRescepid(int rescepid) {
	this.rescepid = rescepid;
}
public String getRname() {
	return rname;
}
public void setRname(String rname) {
	this.rname = rname;
}
public Receptionist(int rescepid, String rname) {
	super();
	this.rescepid = rescepid;
	this.rname = rname;
}
   
 
}
